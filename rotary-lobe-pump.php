            <li data-id="id-3" data-type="faqs">
            	<h2>FAQ's</h2>
                <p><strong>What precautions should be taken when handling abrasive slurries?</strong><br>
                Select a large pump, running at low speed.  The circumferential speed of the rotor is very important, to minimise wear between rotor and casing use a hardened casing to create the best possible wear resistance, together with rotor materials that can withstand the abrasion.</p>
                
                <p><strong>What precautions should be taken when handling high viscosity fluids?</strong><br>
                Make sure that the pump will be filled up.  This is achieved by selecting a pump with large ports, which allows the fluid to fill the pump chambers.  Running the pump at low speed gives the fluid enough time to enter the pump chambers. 
                If a satisfactory degree of filling can not be obtained by gravity or pressuring the fluid source tank, an in-feed screw may be required. 
                Install the pump as close as possible to the fluid source, avoiding unnecessary restrictions. 
                If these issues are not addressed, the pump may cavitate which will increase wear and may inflict damage to the pump.</p>
                
                <p><strong>Can the pump withstand dry running? </strong><br>
                The lobe pump may dry run, as there is no physical contact between rotors and casing.  The seal chamber is filled with a lubricant, that will not let the seals dry run, but keep them lubricated at all times.  Further to this the lubricant will transmit heat generated away from the seals.</p>
                
                <p><strong>How can I minimise the pressure drop?</strong><br>
                Use long elbows, and large diameter pipes.  Use as few elbows as possible.  In certain cases lubricants can be added to the fluid, thereby decreasing the pressure drop.
                One should remember that the pipe system is a one-time cost - the increased running cost caused by high pressure is a continuous cost through the complete life span of the pump.</p>
