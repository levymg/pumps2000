            <h2>Pittsburgh, 10/9/2013</h2>
            
            <p>Pumps 2000 America is now offering the latest technology in diaphragm pumps by introducing the 3&rdquo; HV High-Volume Ball Valve Pump. This air-operated, dual-diaphragm pump has a flow rate of 417 gpm &mdash; significantly greater volume at every point on the curve than competing models are capable of handling. Although the outer chamber and ports are 3 inches, this pump uses a 4-inch patented air motor which contains the best features of the Pumps 2000 line, utilizing new technology for increased efficiency. The 3" HV High-Volume Ball Valve Pump is ideal for the most demanding applications in many industries, including mine &amp; tunneling, chemical manufacturing, construction, food processing, general manufacturing, marine, oil &amp; gas production, pollution control, pulp &amp; paper manufacturing, railroad, sanitation, and water &amp; wastewater treatment.</p>

			<p>&ldquo;The 3&rdquo; HV Ball Valve Pump will out-perform and outlast all others due to a number a number of patented design features,&rdquo;  says Lou Beatty, director of sales and marketing. &ldquo;Several patented features make it possible for Pumps 2000 to offer market-leading low life cycle cost while delivering outstanding performance in the harshest environments as these pumps are designed specifically to handle abrasive, solid-laden and corrosive fluids.&rdquo; Key features and benefits include:</p>
			
