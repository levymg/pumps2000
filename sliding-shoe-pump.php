                <h4>Piping</h4>
                <p>The size of pipe should not be smaller than the pump ports and, for long pipe runs or when handling viscous liquids should generally be larger.  For other than very simple pipe runs, the actual pipe losses should be calculated using loss-of-head curves, as they may be a major part of the total head on the pump.  Easy bends should be used and sharp elbows and tees avoided, to avoid loss of capacity or difficulty in priming due to air leaks.</p>
                 
                <h4>Net Positive Suction Head (NPSH)</h4>
                <p>NPSH available is the absolute pressure of the liquid at the pump entry less its vapour pressure at the pumping temperature.  It is an expression of the service conditions and is the maximum absolute pressure available at the pump entry to overcome the suction losses of the pump itself.</p>
                <p>NPSH required is the minimum absolute pressure necessary at the pump entry to maintain the required flow without cavitation.  The NPSH required for any pump is the standard absolute atmospheric pressure (10.4 m or 34 ft of water) less the listed suction lift of the pump at normal temperature and pressure.</p>
                
                <h4>Specific Gravity</h4>
                <p>The specific gravity of a liquid is the ratio of the weight of unit volume of the liquid to that of distilled water at 4oC.  For normal pumping calculations, the specific gravity of clean fresh water can be taken as 1, and that of sea water 1.03.</p>
                 
                <h4>Viscosity</h4>
                <p>In the SI system of units, <strong>dynamic viscosity</strong> is expressed as the Newton second per square meter (N*s/m2.  In the CGS system of units, dynamic viscosity is expressed as dyne second per square centimetre (dyne*s/cm2) which is called a Poise.</p>
                <p>The centipoise (cP), is a hundredth of a Poise and is the most common unit for dynamic viscosity.  The dynamic viscosity for water at 20oC (68&deg;F) is approximately 1 cP.</p>
                
                <p><strong>Kinematic viscosity</strong> is defined as the ratio of dynamic viscosity to density.  The most common unit for kinematic viscosity is the centistoke (cSt), which is a hundredth of a Stoke.  A Stoke is defined as 1 cm2/s and is taken from : </p>
                
                <p>kinematic viscosity = dynamic viscosity ÷ density  =  Poise ÷ g/cm3</p>
                
