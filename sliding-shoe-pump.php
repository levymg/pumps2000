                <p>The capacity of each Megator positive displacement pump is directly proportional to the pump speed.  Therefore any capacity can be interpolated from the tables above.  The pump curve is therefore a straight line.</p>
                
                <h3>Example</h3>
                <p><strong>Application = clean water</strong></p>
                
                <p>Total head = 22 metres<br />
                Capacity required = 8m3/hr</p>
                
                <ol class="list">
                    <li>From the clean liquids table select a pump with a maximum total head equal to or greater than total head required</li>
                    <li>Select a pump with a capacity equal to or greater than the capacity required</li>
                    <li>Divide the listed pump speed by the listed capacity</li>
                    <li>Multiply answer by the required capacity</li>
                </ol>
                
                <p>L150 = 10m<sup>3</sup>/hr @ 1370 rpm<br />
                1370 &divide; 10 = 137<br />
                8m<sup>3</sup>/hr x 137 = 1096 rpm</p>
                
                <p>Pump required = L150 @ 1096 rpm<br />
                Giving 8m3/hr at a maximum head of 30 metres</p>
                
                <p>The Megator sliding shoe pump delivers its rated capacity at any head and at any suction lift within its range. The head developed is the head imposed by the system at the rated flow. A head capacity curve on the conventional basis would be a vertical straight line.</p>

                <h4>Intermediate Capacities</h4>
                <p>For intermediate capacities, the pumps can be run at speeds lower than those listed in the tables. The head is independent of the speed. The capacity, and the horsepower at a given head, are approximately proportional to the speed.</p>
                
                <h4>Motor Ratings</h4>
                <p>The standard motors listed provide for the maximum heads and, in the case of type H pumps, for an intermediate head. Where the heads are substantially below those listed , smaller motors can frequently be used.</p>
                
                <h4>Suction Lift</h4>
                <p>The suction lifts listed assume normal temperature. Installations combining suction lift with temperatures above normal should be referred to Megator.</p>
                
                <h4>Temperature</h4>
                <p>The ratings listed in the tables are valid for temperatures up to  38&fnof;C (100&deg;F), subject to the above note on suction lift. Where higher temperatures are involved, it is advisable to consider them in conjunction with the other working conditions and such cases should be referred to Megator.</p>
                
                <h4>Dirty Liquids</h4>
                <p>Reduced pump speeds give a markedly increased resistance to wear, and the high suction ratings are recommended for dirty liquids. Under severe conditions still lower speeds may provide the most economical installation.</p>
                
