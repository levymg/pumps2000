            	<h2>Frequently Asked Questions</h2>
                <p><strong>Does the sliding shoe pump require an additional priming device?</strong><br>
				No, self-priming of the sliding shoe pump is spontaneous, without the use of an additional priming device. The only requirement is that the pump is filled with liquid before first start, or after a long shutdown.</p>
                <p><strong>Will the sliding shoe pump be damaged if left running on a dry suction?</strong><br>
				No, the sliding shoe pump remains full of liquid even when it has a completely dry suction. It can run for long periods without damage.</p>
                <p><strong>Does the suction pipe require a foot-valve when using the sliding shoe pump?</strong><br>
				No, the sliding shoe pump will displace any amount of air from the suction pipe.</p>
                <p><strong>Can an isolating valve be fitted to the discharge pipework?</strong><br>
				Yes, any positive displacement pump will develop a dangerously high pressure, if run with the discharge throttled or shut off. If an isolating valve is fitted to the suction pipework (or if it is possible for an excessive head to be imposed on the pump by any other cause), then the pump must be fitted with a pressure relief valve, which is available as an option on all sliding shoe pumps.</p>
                <p><strong>Can a star-delta starter be used on electric driven sliding shoe pumps?</strong><br>
				No, when changing from star to delta, there is - for a fraction of a second - a stage where the pump will stop and would effectively be starting the pump in delta. We would recommend using direct-on-line starters or 'soft start' starters.</p>
                <p><strong>What type of suction strainer should be used with the sliding shoe pump?</strong><br>
				The suction pipe must be fitted with a strainer of adequate size. The total area of the holes should be approximately three to four times the cross sectional area of the pipe. The <a href="floating-strainers.php">Megator Dolphin strainer</a> is ideally suited to this application.</p>
                <p><strong>Can Megator pumps run in parallel?</strong><br>
				All Megator pumps will operate in parallel with one another, or with other pumps, but certain points should be observed with regards to the piping. A common suction pipe to two or more pumps may cause one pump to interfere with the effective priming of another. each pump should preferably have an individual suction line. If this is not possible, each pump should be fitted with a non return valve.</p>
