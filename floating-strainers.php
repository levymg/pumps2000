                <h4>Suction Hose</h4>
                
                <p>A suction hose of 10 ft (3 m) long is recommended.  The floating strainer will support this length of hose in the water, the other end resting on the ground or attached to a fixed pipe.</p>

				<p>The floating strainer normally floats with the hose hanging in a loop.  part of the strainer may be above the water surface, but air will not be drawn in, as the entry tube is well submerged.  In shallow water a longer hose is practicable as it rests partly the bottom.</p>
                
                <img src="images/skimmer2.jpg" class="right" />
                <h4>Large rise and fall</h4>
                
                <p>If there is a large rise and fall of the water level, and particularly if the length of hose is restricted, the fixed end of the hose should be only a little above the low water level. It does not generally matter if the strainer is completely submerged at high level.</p>
                
                <img src="images/skimmer3.jpg" class="right" />
                <h4>Foot valve</h4>
                
                <p>If a foot valve is required, due to the use of a non-self priming pump, it should be fitted at the lowest point of the suction piping.  It is not advisable to fit a foot valve in or adjoining the strainer, as this could form an air lock in the hose.</p>
                
